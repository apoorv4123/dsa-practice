package L2_feb1;

import java.util.Scanner;

public class pattern7 {

	public static void main(String[] args) {
		Scanner s=new Scanner(System.in);
		 int n=s.nextInt();
		 int row=1;
		 while(row<=n) {
			 for(int j=1;j<=n;j++) {
				 System.out.print("* ");
			 }
			 System.out.println();
	}
  }
}
